# a condition is an expression that evaluates to a boolean value, either True or False.
# a statement will only execute if it evaluates to True.

if 5 > 3:
    print("5 is more than 3")
    
if 6 > 10:
    print("This is false, will not be printed")


if "Jason" == "Jason":
    print("Great name!")
    
if "jason" != "Jason":
    print("This prints because the strings are not the same")
    
if True:
    print("always True, always prints")
if False:
    print("never True, never prints")