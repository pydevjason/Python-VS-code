# tuples are similar to lists, however, tuples are immutable and are useful for data that does not change.

types_of_plants = ("algae", "conifers", "cacti", "moss")
print(types_of_plants)
print()

empty_tup = ()
print(type(empty_tup))
print()

number = 1,
print(type(number))
print()

print(tuple(["changing", "to", "tuple"]))
print()

