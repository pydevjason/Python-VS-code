# Inheritance is a design pattern in which a class inherits or recieves attributes and methods from one or more other classes
# Inheritance helps to organize related classes and reduce duplication

# The class being inherited from is called the parent class, superclass, or base class

# The class that inherits is the derived class or child class or subclass

# So what is inherited?
# public and protected attributes beginning with a single underscore are inherited by the subclass as are dunder methods.  Private, name-mangled attributes beginning with double underscores are not inherited by the subclass

# can be thought of a type of relationship such as:
# a CoffeeShop is a type of Store
# a Horse is a type of Animal
# a Train is a type of Vehicle
# an ElectricGuitar is a type of Guitar

